# Variable

first_name = 'Marcelo'

print(first_name)

last_name = 'Santos'

print(last_name)

full_name = first_name + ' ' + last_name

print(full_name)

# Constant

PI = 3.14159

print(PI)

GRAVITY = 9.81

print(GRAVITY)

SPEED_OF_LIGHT = 299792458

print(SPEED_OF_LIGHT)

# Variable reassignment

first_name = 'Maria'

print(first_name)

PI = 3.14

print(PI)

